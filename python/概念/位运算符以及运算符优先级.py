#位运算符，将数据转为2进制计算
print(4&8)#按 位与 &,同为1时是1，否则为0
print(bin(4),bin(8))
#该符号用二进制计算

print(4|8)#按 位或 |,同为0时是0否则为1
print(bin(4|8))

#位移符号
print(bin(4<<1))#左移1位，高位不为1时相当于乘2
print(bin(4<<2))#左移2位
print(bin(4>>1))#右移1位，低位不为1时相当于除以2

#运算符优先级为
# **>*,/,//,%>+,->'<<,>>'>&,|> '>,<,>=,<=,==,!=' >'and,or'>=
#         算术运算|  位运算  |   比较运算        |布尔运算|赋值运算
